     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        $this->validate($request, Post::$rules);
        
        $post = new Post;
        $form = $request->all();
        
        unset($form['_token']);
        
        $post->fill($form);
        $post->user_id = Auth::id();
        $post->save();
        
        return redirect('/posts');
    }

